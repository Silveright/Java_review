package ex05_5_for;

public class ForEx1_1 {

	public static void main(String[] args) {
		int cnt;
		for ( cnt =1; cnt<=10; cnt++) {
			System.out.println(cnt);
		}
		System.out.println(cnt);
		
		System.out.println("============");
		
		for ( cnt = 10; cnt>=0;cnt--) {
			System.out.println(cnt);
		}
	}

}
